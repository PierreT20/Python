import turtle
t = turtle.Turtle()

a = 400
for i in range(10):
    a = a - 20
    t.forward(a)
    t.left(90)
    t.forward(a)
    t.left(90)
    t.forward(a)
    t.left(90)
    t.forward(a)
    t.left(90)
    t.up()
    t.forward(10)
    t.left(90)
    t.forward(10)
    t.right(90)
    t.down()
   

